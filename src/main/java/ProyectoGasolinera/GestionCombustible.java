    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        try {
            // Consulta SQL para obtener todos los combustibles
            String consulta = "SELECT CombustibleID, Tipo, PrecioPorLitro, FechaActualizacion FROM Combustible";
            PreparedStatement pstmt = conexion.conn.prepareStatement(consulta);
            ResultSet rs = pstmt.executeQuery();

            // Limpiar tabla antes de agregar nuevos datos
            DefaultTableModel modelo = (DefaultTableModel) GasolinaTabla.getModel();
            modelo.setRowCount(0); // Elimina todas las filas existentes

            // Recorrer el ResultSet y agregar cada fila a la tabla
            while (rs.next()) {
                Object[] fila = new Object[4]; // Suponiendo que tienes 4 columnas
                fila[0] = rs.getInt("CombustibleID");
                fila[1] = rs.getString("Tipo");
                fila[2] = rs.getDouble("PrecioPorLitro");
                fila[3] = rs.getDate("FechaActualizacion"); // Ajusta el nombre del campo según tu base de datos
                modelo.addRow(fila);
            }

            // Cerrar ResultSet y PreparedStatement
            rs.close();
            pstmt.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al listar los combustibles: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnListarActionPerformed

