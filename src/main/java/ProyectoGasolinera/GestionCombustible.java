    private int obtenerIDSeleccionado() {
        int filaSeleccionada = GasolinaTabla.getSelectedRow();
        if (filaSeleccionada != -1) { // Verifica si hay una fila seleccionada
            // Obtener el valor de la columna CombustibleID de la fila seleccionada
            int combustibleID = (int) GasolinaTabla.getValueAt(filaSeleccionada, 0); // Suponiendo que CombustibleID está en la columna 0
            return combustibleID;
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecciona un combustible de la tabla.");
            return 0; // Otra forma de manejar el caso cuando no hay fila seleccionada
        }
    }

    private void ejecutarProcedimiento(String tipoCombustible, double precioLitro, int opcion, int combustibleID) {
        try {
            String procedimiento = "{call Combustible(?, ?, ?, ?)}";
            CallableStatement stmt = conexion.conn.prepareCall(procedimiento);
            stmt.setString(1, tipoCombustible);
            stmt.setDouble(2, precioLitro);
            stmt.setInt(3, opcion);
            stmt.setInt(4, combustibleID);

            stmt.execute();

            switch (opcion) {
                case 1:
                    JOptionPane.showMessageDialog(null, "Combustible agregado correctamente.");
                    break;
                case 2:
                    JOptionPane.showMessageDialog(null, "Combustible eliminado correctamente.");
                    break;
                case 3:
                    JOptionPane.showMessageDialog(null, "Combustible actualizado correctamente.");
                    break;
                case 4:
                    // Lógica para manejar el resultado de SELECT si es necesario
                    break;
                default:
                    JOptionPane.showMessageDialog(null, "Operación desconocida.");
                    break;
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al ejecutar el procedimiento almacenado: " + ex.getMessage());
        }
    }

