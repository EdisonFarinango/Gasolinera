    public double obtenerPrecioPorLitro(String tipoCombustible) throws SQLException {
        double precioPorLitro = 0.0;
        String sql = "SELECT PrecioPorLitro FROM Combustible WHERE Tipo = ?";

        try {
            // Establecer conexión
            conexion.conectar();

            // Preparar la declaración SQL
            PreparedStatement stmt = conexion.conn.prepareStatement(sql);
            stmt.setString(1, tipoCombustible);

            // Ejecutar la consulta
            ResultSet rs = stmt.executeQuery();

            // Verificar si se encontró el precio por litro
            if (rs.next()) {
                precioPorLitro = rs.getDouble("PrecioPorLitro");
            }

            // Cerrar recursos
            rs.close();
            stmt.close();
        } catch (SQLException ex) {
            System.out.println("Error al obtener precio por litro: " + ex.getMessage());
            throw ex;
        } finally {
            // Desconectar siempre después de usar
            conexion.desconectar();
        }

        return precioPorLitro;
    }

