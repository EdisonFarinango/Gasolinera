    private boolean camposEstanLlenos() {
        return !fieldNombre.getText().trim().isEmpty()
                && !fieldApellido.getText().trim().isEmpty()
                && !fieldCedula.getText().trim().isEmpty()
                && !fieldTelefono.getText().trim().isEmpty()
                && !fieldDireccion.getText().trim().isEmpty();

    }

    public void mostrarClientes() {
        try {
            ResultSet rs = cliente.obtenerTodosClientes();
            DefaultTableModel model = (DefaultTableModel) tablaClientes.getModel();

            // Limpiar la tabla antes de llenarla
            model.setRowCount(0);

            while (rs.next()) {
                Object[] row = new Object[9]; // Número de columnas que esperas
                row[0] = rs.getString("Cedula");
                row[1] = rs.getString("Nombre");
                row[2] = rs.getString("Apellido");
                row[3] = rs.getString("Telefono");
                row[4] = rs.getString("Direccion");
                row[5] = rs.getString("Marca");
                row[6] = rs.getString("Placa");
                row[7] = rs.getString("anioFabricacion");
                row[8] = rs.getString("Color");

                model.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            System.out.println("Error al ejecutar la operación: " + e.getMessage());
        }

    }


