    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked

        String cedula = fieldCedula.getText().trim();
        if (!cedula.isEmpty()) {
            try {
                ResultSet rs = cliente.buscarClientePorCedula(cedula);
                if (rs.next()) {
                    // Mostrar los datos del cliente y vehículo en los campos correspondientes
                    fieldNombre.setText(rs.getString("Nombre"));
                    fieldApellido.setText(rs.getString("Apellido"));
                    fieldCedula.setText(rs.getString("Cedula"));
                    fieldTelefono.setText(rs.getString("Telefono"));
                    fieldDireccion.setText(rs.getString("Direccion"));
                    fieldPlaca.setText(rs.getString("Placa"));
                    fieldFabricacion.setText(rs.getString("AnioFabricacion"));
                    // Habilitar botones de actualizar y borrar
                    btnUpdate.setEnabled(true);
                    btnDelete.setEnabled(true);
                    btnInsert.setEnabled(false);
                    fieldCedula.setEnabled(false);
                    fieldPlaca.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró ningún cliente con la cédula proporcionada.");
                    // Habilitar botón de insertar
                    btnInsert.setEnabled(true);
                    btnUpdate.setEnabled(false);
                    btnDelete.setEnabled(false);
                }
                // Mostrar todos los clientes en la tabla
                mostrarClientes();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error al buscar el cliente: " + ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese la cédula del cliente a buscar.");
        }


    }//GEN-LAST:event_btnBuscarMouseClicked

    private void llenarCamposDesdeTabla(int filaSeleccionada) {
        DefaultTableModel modelo = (DefaultTableModel) tablaClientes.getModel();

        // Obtener datos de la fila seleccionada
        String cedula = (String) modelo.getValueAt(filaSeleccionada, 0); // Suponiendo que la cédula está en la columna 0
        String nombre = (String) modelo.getValueAt(filaSeleccionada, 1); // Suponiendo que el nombre está en la columna 1
        String apellido = (String) modelo.getValueAt(filaSeleccionada, 2); // Suponiendo que el apellido está en la columna 2
        String telefono = (String) modelo.getValueAt(filaSeleccionada, 3); // Suponiendo que el teléfono está en la columna 3
        String direccion = (String) modelo.getValueAt(filaSeleccionada, 4); // Suponiendo que la dirección está en la columna 4
        String marca = (String) modelo.getValueAt(filaSeleccionada, 5); // Suponiendo que la dirección está en la columna 4
        String placa = (String) modelo.getValueAt(filaSeleccionada, 6); // Suponiendo que la dirección está en la columna 4
        String aniofabricacion = (String) modelo.getValueAt(filaSeleccionada, 7); // Suponiendo que la dirección está en la columna 4
        String color = (String) modelo.getValueAt(filaSeleccionada, 8); // Suponiendo que la dirección está en la columna 4

        // Llenar campos de texto con los valores obtenidos
        fieldCedula.setText(cedula);
        fieldNombre.setText(nombre);
        fieldApellido.setText(apellido);
        fieldTelefono.setText(telefono);
        fieldDireccion.setText(direccion);
        fieldPlaca.setText(placa);
        fieldFabricacion.setText(aniofabricacion);
        comboMarca.setSelectedItem(marca);
        comboColor.setSelectedItem(color);
        // Habilitar botones de actualizar y borrar
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        btnInsert.setEnabled(false);
        fieldCedula.setEnabled(false);
        fieldPlaca.setEnabled(false);
    }

