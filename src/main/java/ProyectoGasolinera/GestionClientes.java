    private void ejecutarSP(int operacion) {
        String nombre = fieldNombre.getText().trim();
        String apellido = fieldApellido.getText().trim();
        String cedula = fieldCedula.getText().trim();
        String telefono = fieldTelefono.getText().trim();
        String direccion = fieldDireccion.getText().trim();

        try {
            ResultSet rs = cliente.ejecutarSP(operacion, cedula, nombre, apellido, telefono, direccion);
            if (operacion == 1) {
                mostrarClientes();
            } else {
                if (operacion == 2) {
                    JOptionPane.showMessageDialog(this, "Empleado insertado correctamente.");
                } else if (operacion == 3) {
                    JOptionPane.showMessageDialog(this, "Empleado actualizado correctamente.");
                } else if (operacion == 4) {
                    JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente.");
                }

            }
        } catch (SQLException ex) {
            System.out.println("Error al ejecutar la operación: " + ex.getMessage());
        }
    }

    private void ejecutarSPvehiculo(int operacion) {
        String placa = fieldPlaca.getText().trim();
        String cedula = fieldCedula.getText().trim();
        String marca = (String) comboMarca.getSelectedItem();
        String anio_fabricacion = fieldFabricacion.getText().trim();
        String color = (String) comboColor.getSelectedItem();

        // Validar campos obligatorios
        if (placa.isEmpty() || cedula.isEmpty() || marca == null || anio_fabricacion.isEmpty() || color == null) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos antes de continuar.");
            return;
        }

        try {
            ResultSet rs = vehiculo.ejecutarSPVehiculo(operacion, placa, cedula, marca, anio_fabricacion, color);
            if (operacion == 1) {
                mostrarClientes();
            } else {
                if (operacion == 2) {
                    JOptionPane.showMessageDialog(this, "Vehículo insertado correctamente.");
                } else if (operacion == 3) {
                    JOptionPane.showMessageDialog(this, "Vehículo actualizado correctamente.");
                } else if (operacion == 4) {
                    JOptionPane.showMessageDialog(this, "Vehículo eliminado correctamente.");
                }
            }
        } catch (SQLException ex) {
            System.out.println("Error al ejecutar la operación: " + ex.getMessage());
        }
    }

