    // Método para manejar el evento de los botones numéricos
    private void botonPresionado(String numero) {
        String valorActual = fieldPrecioIngresado.getText();
        fieldPrecioIngresado.setText(valorActual + numero);

        // Llamar al método calcularTotal() para actualizar el total
        calcularTotal();
    }

// Método para calcular el total basado en el valor ingresado en fieldPrecioIngresado
    private void calcularTotal() {
        try {
            // Obtener el precio por litro del tipo de gasolina seleccionado
            String tipoGasolina = (String) comboGasolina.getSelectedItem();
            if (tipoGasolina == null || tipoGasolina.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Seleccione un tipo de gasolina válido.");
                return;
            }

            double precioLitro = gestionCombustible.obtenerPrecioPorLitro(tipoGasolina);

            // Obtener la cantidad ingresada en fieldPrecioIngresado
            String cantidadIngresadaStr = fieldPrecioIngresado.getText().trim();
            if (cantidadIngresadaStr.isEmpty()) {
                System.out.println("Ingrese una cantidad válida.");
                return;
            }

            double cantidadIngresada = Double.parseDouble(cantidadIngresadaStr);

            // Calcular el total
            double total = precioLitro * cantidadIngresada;

            // Mostrar el total en el campo fieldTotal
            fieldTotal.setText(String.valueOf(total));
        } catch (SQLException ex) {
            Logger.getLogger(Facturas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al obtener el precio del combustible: " + ex.getMessage());
        } catch (NumberFormatException ex) {
            Logger.getLogger(Facturas.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Ingrese una cantidad válida.");
        }
    }

    private void cargarTiposCombustible() {
        try {
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            // Obtener tipos de combustible desde la base de datos
            ResultSet rs = gestionCombustible.obtenerTiposCombustible();
            while (rs.next()) {
                String tipoCombustible = rs.getString("Tipo");
                model.addElement(tipoCombustible);
            }
            comboGasolina.setModel(model);

            // Llamar al método calcularTotal() inicialmente para mostrar un total inicial si es necesario
            calcularTotal();
        } catch (SQLException ex) {
            Logger.getLogger(Facturas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

