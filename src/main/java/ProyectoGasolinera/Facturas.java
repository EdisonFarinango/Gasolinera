    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String cedula = fieldCedula.getText();
        String tipoGasolina = (String) comboGasolina.getSelectedItem();
        double total = Double.parseDouble(fieldTotal.getText());
        double cantidadLitros = Double.parseDouble(fieldPrecioIngresado.getText());

        try {
            conexion.conectar();

            int estacionID = ConsultasSQL.obtenerUltimoEstacionID(conexion);
            int combustibleID = ConsultasSQL.obtenerIDCombustible(conexion, tipoGasolina);

            insertarFactura(estacionID, total, cedula, combustibleID, cantidadLitros);

            // Obtener datos del cliente
            String nombreCompleto = fieldNombre.getText();
            String direccion = fieldDireccion.getText();
            String telefono = fieldTelefono.getText();

            // Crear una instancia de FacturaDetalle y pasar los datos
            FacturaDetalle facturaDetalle = new FacturaDetalle(nombreCompleto, direccion, telefono, cedula, tipoGasolina, cantidadLitros, total);
            facturaDetalle.setVisible(true);
            this.dispose();

            JOptionPane.showMessageDialog(this, "Factura guardada correctamente.");
            limpiarCampos();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar la factura: " + ex.getMessage());
            ex.printStackTrace();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese valores numéricos válidos.");
            ex.printStackTrace();
        } finally {
            conexion.desconectar();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    

    
    
    private void insertarFactura(int estacionID, double total, String cedula, int combustibleID, double cantidadLitros) throws SQLException {
        String sql = "INSERT INTO facturas (Fecha, EstacionID, Total, Cedula, CombustibleID, CantidadLitros) "
                + "VALUES (CURDATE(), ?, ?, ?, ?, ?)";

        try (PreparedStatement pstmt = conexion.conn.prepareStatement(sql)) {
            pstmt.setInt(1, estacionID);
            pstmt.setDouble(2, total);
            pstmt.setString(3, cedula);
            pstmt.setInt(4, combustibleID);
            pstmt.setDouble(5, cantidadLitros);

            pstmt.executeUpdate();
        }
    }

